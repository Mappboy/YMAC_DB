$("#requiredBy").formValidation({
    framework: 'bootstrap',
    icon: {
        valid: 'glyphicon glyphicon-ok',
        invalid: 'glyphicon glyphicon-remove',
        validating: 'glyphicon glyphicon-refresh'
    },

    fields: {
        element_10: {
            validators: {
                notEmpty: {
                    message: 'The date is required'
                },
                date: {
                    format: 'DD/MM/YYYY',
                },
                callback: {
                    message: 'The date is before today',
                    callback: function (value, validator) {
                        var m = new moment(value, 'DD/MM/YYYY', true);
                        if (!m.isValid()) {
                            return false;
                        }
                        return m.isAfter(moment().subtract(1, 'days'));
                    }
                }
            }
        }
    }
});
$('#dateRangePicker').on('dp.change dp.show dp.update', function (e) {
    $('#requiredBy').formValidation('revalidateField', 'element_10');
});
